<script type="text/javascript">

    // Auto select industry if present
    if ( getParameterByName('industry') ) {
        var industry = getParameterByName('industry');
        var option = document.querySelectorAll('[data-slug='+industry+']');
        if ( option[0] ) {
            var index = option[0].index;
            var select = document.getElementById("select_industry");
            select.selectedIndex = index;
            changeSubject(select);
        }
    }

    function changeSubject(select) {
        var value = select.options[select.selectedIndex].value;
        document.getElementById("subject").value = value;
    }
    function getParameterByName(name) {
        name = name.replace(/[\[]/, "\\[").replace(/[\]]/, "\\]");
        var regex = new RegExp("[\\?&]" + name + "=([^&#]*)"),
            results = regex.exec(location.search);
        return results === null ? "" : decodeURIComponent(results[1].replace(/\+/g, " "));
    }
